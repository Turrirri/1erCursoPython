nombre = 'Jhonny'

# concatenar con +
bienvenida = "Hola " + nombre + " ¿Cómo estas?"  # Al concatenar con '+' hay que estar pendiente de dejar espacios
print(bienvenida)

# concatenar con f-strings
bienvenida = f"Hola {nombre} ¿Cómo estás?"
print(bienvenida)

# operadores de pertenecia (in / not in)
print("nny" in bienvenida)
print("Jhonny" not in bienvenida)
